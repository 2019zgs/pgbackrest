####################################################################################################################################
# EXCEPTION MODULE
####################################################################################################################################
package BackRest::Exception;

use threads;
use strict;
use warnings;
use Carp;

use Moose;

# Module variables
has iCode => (is => 'bare');       # Exception code
has strMessage => (is => 'bare');  # Exception message

####################################################################################################################################
# CODE
####################################################################################################################################
sub code
{
    my $self = shift;

    return $self->{iCode};
}

####################################################################################################################################
# MESSAGE
####################################################################################################################################
sub message
{
    my $self = shift;

    return $self->{strMessage};
}

no Moose;
__PACKAGE__->meta->make_immutable;
